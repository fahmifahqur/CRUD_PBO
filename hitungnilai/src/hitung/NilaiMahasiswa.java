    private void btnPiechartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPiechartActionPerformed
        // TODO add your handling code here:
        String nama = nama_mhs.getText();
        String tugas = tugas_mhs.getText();
        String uts = uts_mhs.getText();
        String uas = uas_mhs.getText();
        Integer iTugas = Integer.valueOf(tugas);
        DefaultPieDataset dataset = new DefaultPieDataset();
        dataset.setValue("Tugas", iTugas);
        dataset.setValue("UTS", Integer.valueOf(uts));
        dataset.setValue("UAS", Integer.valueOf(uas));
        
        JFreeChart chart1 = ChartFactory.createPieChart(nama, dataset, false, false, false);
        PiePlot plot2 = (PiePlot) chart1.getPlot();
        ChartFrame frame = new ChartFrame("Perbandingan nilai "+nama,chart1);
        frame.setVisible(true);
        frame.setSize(450,500);
    }//GEN-LAST:event_btnPiechartActionPerformedz
